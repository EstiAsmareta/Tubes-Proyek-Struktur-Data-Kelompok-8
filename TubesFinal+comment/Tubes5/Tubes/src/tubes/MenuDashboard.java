    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        // fungsi ini adalah jika kita mengklik tombol search maka akan menampilkan data mahasiswa yang dicari
        LinkListData data = dataMhs.head; // awalan node
        boolean bool = false;
        //membuat looping sampai salah satu data = key maka boolean akan bernilai true dan looping akan berhenti
        while(data != null){
            if(cari.getText().equals(data.Nama)){
                bool = true;
                break;
            }
            data = data.next;
        }
        //pengecekan jika sudah di search atau data sudah ditemukan maka membuat object
        //baru data mahasiswa baru dan mengirimkan object data ke dalamnya.
        if(bool){
        new DataMahasiswaBaru(data).setVisible(true);
        
            bool = false;
        }
    }//GEN-LAST:event_btnSearchMouseClicked

    
