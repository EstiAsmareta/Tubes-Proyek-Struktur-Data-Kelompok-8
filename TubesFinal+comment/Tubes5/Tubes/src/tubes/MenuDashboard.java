    public void editData(){
        // untuk mengedit data yang dipilih di row untuk dirubah
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRow()==-1){
            if(jTable1.getSelectedRowCount()==0){
                JOptionPane.showMessageDialog(null, "Tidak ada data dalam tabel");
            }else{
                JOptionPane.showMessageDialog(null, "Pilih data yang akan diubah");
            }
        }else{
            model.setValueAt(txtNama.getText(), jTable1.getSelectedRow(), 0);
            model.setValueAt(txtNIK.getText(), jTable1.getSelectedRow(), 1);
            model.setValueAt(txtTTL.getText(), jTable1.getSelectedRow(), 2);
        }
    }
    
    public void deleteData(){
        //untuk menghapus data yang dipilih dirow
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRow()==-1){
            if(jTable1.getSelectedRowCount()==0){
                JOptionPane.showMessageDialog(null, "Tidak ada data dalam tabel");
            }else{
                JOptionPane.showMessageDialog(null, "Pilih data yang akan diubah");
            }
        }else{
            model.removeRow(jTable1.getSelectedRow());
        }
        reset();
    }

