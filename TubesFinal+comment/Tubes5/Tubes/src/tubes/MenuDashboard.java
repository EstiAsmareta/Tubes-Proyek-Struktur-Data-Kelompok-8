    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
        //digunakan untuk mengadd data hjika kita mengklik tombol simpan
        if(txtNama.getText().equals("") ||txtNIK.getText().equals("") || txtTTL.getText().equals("") || txtAlamat.getText().equals("") || txtEmail.getText().equals("") || txtNomorPendaftaran.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Data yang dimasukkan belum lengkap!");
        }
        else {
            
            //untuk membuat node
            
            LinkListData node = new LinkListData(txtNama.getText(),txtNIK.getText(),txtTTL.getText(),txtAlamat.getText(),txtEmail.getText(),comJenisKelamin.getItemAt(comJenisKelamin.getSelectedIndex()),txtEmail.getText(),txtNomorPendaftaran.getText());
            dataMhs.tambahData(node); // menggabungkan node yang baru saja dibuat
            model.addRow(new Object[]{txtNama.getText(),txtNIK.getText(),txtTTL.getText(),txtAlamat.getText(),comJenisKelamin.getItemAt(comJenisKelamin.getSelectedIndex()), txtEmail.getText(),txtNomorPendaftaran.getText()});
        }
        reset();
    }//GEN-LAST:event_btnSimpanMouseClicked

